//========ATAN========
//Input: y/x , fixed point at 0.1 resolution
//Output: result, -pi/2 <= result <= pi/2
//output in 2^-10 fixed point
int32_t atan(int32_t in){
    bool negative = (in < 0);
    if (negative){in *= -1;}
    if ((in > (255))){
        in = 255;
    }
    return negative ? -aTanTable[in] : aTanTable[in];

}


